package com.example.reto1

class Publication {

    var nameUser:String
    var caption:String
    var date:String
    var location:String
    var URI:String
    var URIprofile:String

    constructor(nameUser:String,caption:String,date:String,location:String,URI:String,URIprofile:String){
        this.nameUser = nameUser
        this.caption = caption
        this.date = date
        this.location = location
        this.URI = URI
        this.URIprofile = URIprofile
    }
}